bool boton_1LeerEstado(){

	uint32_t valor_pin;
	bool resultado;

	valor_pin = GPIO_PinRead(GPIOC, 3);

	if(valor_pin==1){

		resultado = true;
	}
	else{
		resultado = false;
	}

	return resultado;
}

bool boton_2LeerEstado(){

	uint32_t valor_pin;
	bool resultado;

	valor_pin = GPIO_PinRead(GPIOA, 4);

	if(valor_pin==1){

		resultado = true;
	}
	else{

		resultado = false;
	}

	return resultado;

}
